from typing import TypedDict
from langgraph.graph import StateGraph, START, END

class BudgetState(TypedDict):
    financial_data: str
    expense_analysis: str
    budget_plan: str
    savings_plan: str
    final_report: str

def expense_agent(state: BudgetState):

    print("\n" + "="*50)
    print("🔍 EXPENSE ANALYSIS AGENT")
    print("="*50)

    prompt = f"""
You are an Expense Analysis AI Agent.

Analyze the following personal financial information:

{state['financial_data']}

Perform the following tasks:

1. Calculate total monthly expenses.
2. Calculate remaining income.
3. Calculate the percentage of income spent.
4. Identify the three largest expenses.
5. Identify expenses that could potentially be reduced.
6. Provide clear financial observations.

Your output will be passed to another AI agent called
the Budget Agent. Therefore, provide structured and
accurate information.

Do not invent financial data.
"""

    response = llm.invoke(prompt)

    print(response.content)

    return {
        "expense_analysis": response.content
    }

