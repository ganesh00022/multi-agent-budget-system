def report_agent(state: BudgetState):

    print("\n" + "="*50)
    print("📊 FINANCIAL REPORT AGENT")
    print("="*50)

    prompt = f"""
You are a Financial Report AI Agent.

Create a final personal financial report.

USER FINANCIAL DATA:
{state['financial_data']}

EXPENSE ANALYSIS:
{state['expense_analysis']}

BUDGET PLAN:
{state['budget_plan']}

SAVINGS PLAN:
{state['savings_plan']}

Create a professional final report with these sections:

1. Financial Overview
2. Income Summary
3. Expense Analysis
4. Current Financial Position
5. Recommended Monthly Budget
6. Savings Recommendations
7. Final Conclusion

The report should be clear, concise, and easy for
a normal user to understand.

Do not invent any information that is not present
in the provided data.
"""

    response = llm.invoke(prompt)

    print(response.content)

    return {
        "final_report": response.content
    }

