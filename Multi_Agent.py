workflow = StateGraph(BudgetState)

workflow.add_node("expense_agent", expense_agent)

workflow.add_node("budget_agent", budget_agent)

workflow.add_node("savings_agent", savings_agent)

workflow.add_node("report_agent", report_agent)

workflow.add_edge(
    START,
    "expense_agent"
)

workflow.add_edge(
    "expense_agent",
    "budget_agent"
)

workflow.add_edge(
    "budget_agent",
    "savings_agent"
)

workflow.add_edge(
    "savings_agent",
    "report_agent"
)

workflow.add_edge(
    "report_agent",
    END
)

app = workflow.compile()

initial_state = {
    "financial_data": financial_data,
    "expense_analysis": "",
    "budget_plan": "",
    "savings_plan": "",
    "final_report": ""
}

result = app.invoke(initial_state)

print("\n")
print("="*60)
print("           FINAL PERSONAL FINANCIAL REPORT")
print("="*60)

print(result["final_report"])